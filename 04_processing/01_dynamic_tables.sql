FROM TABLE(INFORMATION_SCHEMA.DYNAMIC_TABLE_REFRESH_HISTORY())
WHERE NAME IN ('DT_ORDERS_CLEANED','DT_ORDERS_ENRICHED','DT_SALES_SUMMARY','DT_CUSTOMER_360','DT_PRODUCT_PERFORMANCE')
ORDER BY DATA_TIMESTAMP DESC
LIMIT 20;

